int main(void)
{
    int32_t CH1_DC = 0;
    HAL_Init();
    SystemClock_Config();


    __HAL_RCC_AFIO_CLK_ENABLE();
    __HAL_RCC_PWR_CLK_ENABLE();
    __HAL_RCC_TIM2_CLK_ENABLE();

//    __HAL_AFIO_REMAP_SWJ_DISABLE();


    MX_TIM2_Init();
    MX_GPIO_Init();
    HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);

    while (1)
    {
    	while(CH1_DC < 65535)
    	{
    		TIM2->CCR1 = CH1_DC;
    		CH1_DC += 70;
    		HAL_Delay(1);
    	}
    	//CH1_DC = 65535;
    	while(CH1_DC > 0)
    	{
    	    TIM2->CCR1 = CH1_DC;
    	    CH1_DC -= 70;
    	    HAL_Delay(1);
    	}
    	//CH1_DC = 0;
    }
}



